package com.example;

public class Rectangle {
    private String rect_name;
    private String rect_color;
    private int rect_width;
    private int rect_height;
    
    public String getRect_name() {
        return rect_name;
    }
    public void setRect_name(String rect_name) {
        this.rect_name = rect_name;
    }
    public String getRect_color() {
        return rect_color;
    }
    public void setRect_color(String rect_color) {
        this.rect_color = rect_color;
    }
    public int getRect_width() {
        return rect_width;
    }
    public void setRect_width(int rect_width) {
        this.rect_width = rect_width;
    }
    public int getRect_height() {
        return rect_height;
    }
    public void setRect_height(int rect_height) {
        this.rect_height = rect_height;
    }

    

}
